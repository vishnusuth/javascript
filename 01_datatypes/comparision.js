//comparision operators in javascript are used to compare two values. the result of a comparision is always a boolean (true o false)

//equal (==)
//compares two values for equalit, ignores datatype (does type conversion)

console.log("equal");
console.log(5=="5");
console.log(5==5);

// strict equal (===)
// compares both value and datatype

console.log("Strict Equal");
console.log(5==='5');
console.log(5===5);

//not equal (!=)
//returns true if values are not equal (ignore type)

console.log("not equal");
console.log(5!=5);
console.log(5!=10);


